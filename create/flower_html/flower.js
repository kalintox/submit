function chgUnder(){
	obb = document.getElementById('fon');
	obb.style.textDecoration = "underline";
//	document.getElementById('fon').style.color = 'blue';
 	obb.style.color = 'blue';	
}

function chgSize(){
	obb = document.getElementById('fon');
	obb.style.fontSize = "20pt";	
}

function chgClear(){
	obb = document.getElementById('fon');
	obb.style.textDecoration = "";
	obb.style.fontSize = "";
	obb.style.color = "";
}

